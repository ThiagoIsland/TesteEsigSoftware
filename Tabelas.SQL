CREATE TABLE pessoa (
    id NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR2(255) NOT NULL,
    cidade VARCHAR2(100),
    email VARCHAR2(255),
    cep VARCHAR2(10),
    endereco VARCHAR2(255),
    pais VARCHAR2(50),
    usuario VARCHAR2(50),
    senha_hash VARCHAR2(255),
    telefone VARCHAR2(20),
    data_nascimento DATE,
    cargo_id NUMBER(10),
    CONSTRAINT fk_pessoa_cargo FOREIGN KEY (cargo_id) REFERENCES cargo(id)
);

CREATE TABLE cargo (

    id NUMBER(10) PRIMARY KEY,

    nome VARCHAR2(255) NOT NULL

);

CREATE TABLE vencimentos (

    id NUMBER(10) PRIMARY KEY,

    descricao VARCHAR2(255),

    valor NUMBER(10, 2),

    tipo CHAR(1),

    forma_incidencia CHAR(1)

);

CREATE TABLE cargo_vencimentos (

    id NUMBER(10) PRIMARY KEY,

    cargo_id NUMBER(10),

    vencimentos_id NUMBER(10),

    CONSTRAINT fk_cargo_venc FOREIGN KEY (cargo_id) REFERENCES cargo(id),

    CONSTRAINT fk_venc_cargo FOREIGN KEY (vencimentos_id) REFERENCES vencimentos(id)

); 

CREATE TABLE pessoa_salario (
    pessoa_id NUMBER(10) PRIMARY KEY,
    nome VARCHAR2(255),
    salario_bruto NUMBER(10, 2),
    descontos NUMBER(10, 2),
    salario_liquido NUMBER(10, 2),
    CONSTRAINT fk_pessoa_salario FOREIGN KEY (pessoa_id) REFERENCES pessoa(id)
);